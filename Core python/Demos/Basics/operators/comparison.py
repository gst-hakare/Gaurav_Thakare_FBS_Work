## comparison or relational operator

x = 10
y = 20

#1. == (exact equal to)

print(x == y)
print(x == '10')

#2. != (not equal to)

print(x != 10)
print(x != y)

#3. > (greater than)
print(x > y)

#4. >= (greater than equal to)
print(x >= 10)
print(20 >= 10)

#5. < (less than)
print(x < y)

#6. <= (less than equal to)
print(x <= y)
