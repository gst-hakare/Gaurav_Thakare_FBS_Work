# Variables
#1. Variable is an identifier
#2. Variable is a namespace for memory location

# Rules for variables (Identifiers)
#1. Can contain alphabet (A-Z,a-z), digits (0-9), Underscore (_)
#2. Can't contain symbols Except Underscore
#3. Can't contain space
#4. Can't start with digits
#5. Can't use keywords as a variable names

# Examples
#1. num1 = 10
#2. num@ = 10
#3. num 1 = 10
#4. 1num = 10
#5. if = 10

