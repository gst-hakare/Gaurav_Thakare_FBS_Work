# WAP to calculate area of triangle and rectangle

## Area of traingle

base = float(input("Enter base of traingle : "))
height = float(input("Enter height of traingle : "))

Area_triangle = (1/2) * base * height

print(f'Area of traingle is {Area_triangle}.')



## Area of rectangle

length = float(input("Enter length of rectangle : "))
breadth = float(input("Enter breadth of rectangle : "))

Area_reactangle = length * breadth

print(f'Area of rectangle is {Area_reactangle}.')